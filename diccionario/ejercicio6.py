# Se crea un diccionario llamado 'diccionarioCuadrado' utilizando un bucle 'for' y una expresion de comprension de diccionario las claves del diccionario son numeros del conjunto (3, 5, 6), y los valores son el cubo de cada numero.
diccionarioCuadrado = {x: x**3 for x in (3, 5, 6)}

# Imprimir el diccionario 'dicccionarioCuadrado'.
print(diccionarioCuadrado)