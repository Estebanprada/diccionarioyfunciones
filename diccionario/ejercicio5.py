# Se crea un diccionario llamado 'informacion1' con claves de nombres y sus respectivas notas finales.
informacion1 = {
    'Esteban': 4.5,
    'Paulo': 5.0,
    'Tovar': 4.0,
    'Laura': 2.5
}

# Imprimir las claves del diccionario 'informacion1' utilizando un bucle 'for'.
print("Tecnicas por clave:")
for clave in informacion1.keys():
    print(clave)
# Imprimir los valores del diccionario 'informacion1' utilizando un bucle 'for'.
print("Iterar por valor:")
for valor in informacion1.values():
    print(valor)